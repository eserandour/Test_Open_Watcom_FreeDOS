/* =========================================================
   FONTDATA.C — Données brutes des glyphes bitmap
   =========================================================
   Ce fichier ne contient QUE les définitions de pixels.
   Aucune logique ici : uniquement des appels à defineChar*.

   Comment lire un glyphe 8x8 :
   Chaque octet représente une ligne de 8 pixels.
   Le bit 7 (MSB, 0x80) = pixel le plus à gauche.
   Le bit 0 (LSB, 0x01) = pixel le plus à droite.
   Exemple : 0x3C = 0011 1100 = ..XXXX..

   Comment lire un glyphe 16x16 :
   Chaque unsigned int = une ligne de 16 pixels.
   Le bit 15 (0x8000) = pixel le plus à gauche.
   Exemple : 0xFFF0 = 1111111111110000 = XXXXXXXXXXXX....

   Pour ajouter un glyphe : appeler defineChar8x8/16
   avec le caractère et les données, sans toucher à font.c.

   Pour ajouter une police : créer une nouvelle fonction
   _initFontXD() ici, la déclarer dans fontdata.h, et
   l'appeler depuis un nouvel initMyFontX() dans font.c.
   ========================================================= */

