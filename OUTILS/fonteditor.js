function buildGrid() {
  const gridEl = document.getElementById('grid');
  gridEl.innerHTML = '';
  for (let row = 0; row < MODE; row++) {
    const rowDiv = document.createElement('div');
    rowDiv.className = 'grid-row';
    const lbl = Object.assign(document.createElement('div'), {
      className: 'row-label',
      textContent: row < 10 ? row : String.fromCharCode(55 + row)
    });
    rowDiv.appendChild(lbl);
    for (let col = 0; col < MODE; col++) {
      const cell = document.createElement('div');
      cell.className = 'cell';
      cell.dataset.row = row; cell.dataset.col = col;
      cell.addEventListener('mousedown', e => {
        e.preventDefault(); drawVal = !getPixel(row, col);
        setPixel(row, col, drawVal); isDrawing = true; updateAll();
      });
      cell.addEventListener('mouseenter', () => {
        if (isDrawing) { setPixel(row, col, drawVal); updateAll(); }
      });
      rowDiv.appendChild(cell);
    }
    gridEl.appendChild(rowDiv);
  }
}

function charLabel(c) {
  const code = c.charCodeAt(0);
  if (code < 0x20)  return code.toString(16).toUpperCase().padStart(2,'0');
  if (code === 0x20) return '·';
  if (code === 0x7F) return '7F';
  if (code >= 128 && CP437_NAMES[code]) return CP437_NAMES[code];
  return c;
}

function charTitle(c) {
  const code = c.charCodeAt(0);
  const hex = '0x' + code.toString(16).toUpperCase().padStart(2,'0');
  if (code < 0x20)   return `${hex} (ctrl)`;
  if (code === 0x7F) return `0x7F (DEL)`;
  if (code >= 128)   return `${hex} (${CP437_NAMES[code] || '?'})`;
  return `'${c}' (${hex})`;
}

function buildCharGrid() {
  const gridEl = document.getElementById('char-grid');
  gridEl.innerHTML = '';
  const base  = charPage * 128;
  const slice = CHARS.slice(base, base + 128);
  for (const c of slice) {
    const code = c.charCodeAt(0);
    const cell = document.createElement('div');
    cell.className = 'char-cell';
    cell.title = charTitle(c);
    const lbl = charLabel(c);
    cell.textContent = lbl;
    if (lbl.length > 1 || code < 0x20 || code === 0x7F) cell.style.fontSize = '8px';
    cell.dataset.charCode = code;
    cell.addEventListener('click', () => selectChar(c));
    gridEl.appendChild(cell);
  }
}

