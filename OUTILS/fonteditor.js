function charLiteral(c){
  const code=c.charCodeAt(0);
  if(code>=128||code<0x20||code===0x7F)return String(code);
  if(c==="'")return"'\\''";if(c==="\\")return"'\\\\'";
  return"'"+c+"'";
}
function hex8(b){return'0x'+b.toString(16).toUpperCase().padStart(2,'0');}
function hex16(w){return'0x'+w.toString(16).toUpperCase().padStart(4,'0');}

function generateCode(chars,header=true){
  const lines=[];
  if(MODE===8){
    if(header){lines.push('void _initFont8D(void)');lines.push('{');}
    for(const c of chars){
      const code=c.charCodeAt(0);
      const label=code>=128?(CP437_NAMES[code]||'?'):code<0x20?`0x${code.toString(16).toUpperCase().padStart(2,'0')}`:c===' '?'Espace':code===0x7F?'0x7F':c;
      const comment=label?`  /* ${label} */`:'';
      lines.push(`    defineChar8(myFont8,${charLiteral(c)}, ${font[c].map(hex8).join(',')});${comment}`);
    }
    if(header)lines.push('}');
  } else {
    if(header){lines.push('void _initFont16D(void)');lines.push('{');lines.push('    static unsigned int r[16];');}
    for(const c of chars){
      const code=c.charCodeAt(0);
      const label=code>=128?(CP437_NAMES[code]||'?'):code<0x20?`0x${code.toString(16).toUpperCase().padStart(2,'0')}`:c===' '?'Espace':code===0x7F?'0x7F':c;
      lines.push('');lines.push(`    /* --- ${label} --- */`);
      for(let row=0;row<16;row++){
        const w=((font[c][row*2]<<8)|font[c][row*2+1])&0xFFFF;
        const idx=row.toString().padStart(2,' ');
        const entry=`r[${idx}]=${hex16(w)};`;
        if(row%4===0)lines.push('    '+entry);else lines[lines.length-1]+=entry;
      }
      lines.push(`    defineChar16(myFont16,${charLiteral(c)},r);`);
    }
    if(header)lines.push('}');
  }
  return lines.join('\n');
}

function generateCodeHTML(chars){
  const code=generateCode(chars);
  const e=code.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;');
  return e
    .replace(/\/\*[^*]*\*+(?:[^/*][^*]*\*+)*\//g,m=>`<span class="code-cm">${m}</span>`)
    .replace(/\b(void|return|static|unsigned|int)\b/g,m=>`<span class="code-kw">${m}</span>`)
    .replace(/\b(_initFont8D|_initFont16D|defineChar8|defineChar16)\b/g,m=>`<span class="code-fn">${m}</span>`)
    .replace(/\b(myFont8|myFont16)\b/g,m=>`<span class="code-str">${m}</span>`)
    .replace(/0x[0-9A-Fa-f]+/g,m=>`<span class="code-num">${m}</span>`)
    .replace(/'(?:\\.|[^'\\])'/g,m=>`<span class="code-str">${m}</span>`);
}

