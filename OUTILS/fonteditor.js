function drawGlyphToCanvas(canvas, glyph, m, scale) {
  canvas.width = m*scale; canvas.height = m*scale;
  const ctx = canvas.getContext('2d');
  ctx.fillStyle='#000'; ctx.fillRect(0,0,canvas.width,canvas.height);
  ctx.fillStyle='#fff';
  for(let r=0;r<m;r++) for(let c=0;c<m;c++){
    let on;
    if(m===8){on=!!(glyph[r]&(1<<(7-c)));}
    else{const bi=r*2+(c<8?0:1);on=!!(glyph[bi]&(1<<(7-(c%8))));}
    if(on)ctx.fillRect(c*scale,r*scale,scale,scale);
  }
}

