function getPixel(row, col) {
  if (MODE === 8) return !!(font[current][row] & (1 << (7 - col)));
  const bi = row * 2 + (col < 8 ? 0 : 1);
  return !!(font[current][bi] & (1 << (7 - (col % 8))));
}

function setPixel(row, col, val) {
  if (MODE === 8) {
    if (val) font[current][row] |= (1 << (7-col));
    else      font[current][row] &= ~(1 << (7-col));
  } else {
    const bi = row*2 + (col<8?0:1), bp = 7-(col%8);
    if (val) font[current][bi] |= (1<<bp);
    else      font[current][bi] &= ~(1<<bp);
  }
}

function hasData(c) { return font[c].some(b => b !== 0); }

function selectChar(c) {
  current = c;
  const page = c.charCodeAt(0) < 128 ? 0 : 1;
  if (page !== charPage) setCharPage(page);
  updateAll();
}

function shiftUp() {
  if (MODE===8) font[current]=[...font[current].slice(1),font[current][0]];
  else font[current]=[...font[current].slice(2),...font[current].slice(0,2)];
  updateAll();
}
function shiftDown() {
  if (MODE===8) font[current]=[font[current][7],...font[current].slice(0,7)];
  else font[current]=[...font[current].slice(-2),...font[current].slice(0,-2)];
  updateAll();
}
function shiftLeft() {
  if (MODE===8) { font[current]=font[current].map(b=>((b<<1)|(b>>7))&0xFF); }
  else { const g=font[current]; for(let r=0;r<16;r++){const w=(g[r*2]<<8)|g[r*2+1];const s=((w<<1)|(w>>15))&0xFFFF;g[r*2]=(s>>8)&0xFF;g[r*2+1]=s&0xFF;} }
  updateAll();
}
function shiftRight() {
  if (MODE===8) { font[current]=font[current].map(b=>((b>>1)|(b<<7))&0xFF); }
  else { const g=font[current]; for(let r=0;r<16;r++){const w=(g[r*2]<<8)|g[r*2+1];const s=((w>>1)|((w&1)<<15))&0xFFFF;g[r*2]=(s>>8)&0xFF;g[r*2+1]=s&0xFF;} }
  updateAll();
}
function flipH() {
  if (MODE===8) { font[current]=font[current].map(b=>{let r=0;for(let i=0;i<8;i++)if(b&(1<<i))r|=(1<<(7-i));return r;}); }
  else { const g=font[current]; for(let r=0;r<16;r++){let hi=0,lo=0;for(let i=0;i<8;i++){if(g[r*2+1]&(1<<i))hi|=(1<<(7-i));if(g[r*2]&(1<<i))lo|=(1<<(7-i));}g[r*2]=hi;g[r*2+1]=lo;} }
  updateAll();
}
function flipV() {
  if (MODE===8) { font[current]=[...font[current]].reverse(); }
  else { const g=font[current];const rows=[];for(let r=0;r<16;r++)rows.push([g[r*2],g[r*2+1]]);rows.reverse();for(let r=0;r<16;r++){g[r*2]=rows[r][0];g[r*2+1]=rows[r][1];} }
  updateAll();
}
